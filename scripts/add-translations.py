# MAIN
# ──────────────────────────────────────────────────────────
def main():
    with open(DATA_PATH) as f:
        prayers = json.load(f)

    for p in prayers:
        pid = p["id"]

        # Rename existing fields
        p["title_en"] = p.pop("title")
        p["description_en"] = p.pop("description")
        p["tags_en"] = p.pop("tags")
        if "partialVerse" in p:
            pv_en = p.pop("partialVerse")
            p["partialVerse_en"] = pv_en
            p["partialVerse_ur"] = PARTIAL_VERSE_UR.get(pv_en, pv_en)
            p["partialVerse_hi"] = PARTIAL_VERSE_HI.get(pv_en, pv_en)

        # Add translations
        tr = PRAYER_TRANSLATIONS.get(pid)
        if tr:
            p["title_ur"] = tr["title_ur"]
            p["title_hi"] = tr["title_hi"]
            p["description_ur"] = tr["desc_ur"]
            p["description_hi"] = tr["desc_hi"]
        else:
            # Fallback: use English
            p["title_ur"] = p["title_en"]
            p["title_hi"] = p["title_en"]
            p["description_ur"] = p["description_en"]
            p["description_hi"] = p["description_en"]
            print(f"  ⚠️  No translation for {pid}: {p['title_en']}")

        # Translate tags
        p["tags_ur"] = [TAG_UR.get(t, t) for t in p["tags_en"]]
        p["tags_hi"] = [TAG_HI.get(t, t) for t in p["tags_en"]]

    with open(DATA_PATH, "w") as f:
        json.dump(prayers, f, indent=2, ensure_ascii=False)

    print(f"\n✅ Updated {len(prayers)} prayers with Urdu and Hindi translations")

    # Verify all tags were translated
    all_en = set()
    for p in prayers:
        all_en.update(p["tags_en"])
    missing_ur = [t for t in all_en if t not in TAG_UR]
    missing_hi = [t for t in all_en if t not in TAG_HI]
    if missing_ur:
        print(f"  ⚠️  Tags missing Urdu: {missing_ur}")
    if missing_hi:
        print(f"  ⚠️  Tags missing Hindi: {missing_hi}")

if __name__ == "__main__":
    main()
